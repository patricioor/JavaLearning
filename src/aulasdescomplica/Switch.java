
package aulasdescomplica;

public class Switch {
    public static void main(String[] args) {
        int numero = 3;
        
        switch(numero) {
            case 1:
                System.out.println("caso 1");
                break;
            case 2:
                System.out.println("caso 2");
                break;
            case 3:   
                System.out.println("caso 3");
                break;
        }
    }
}
